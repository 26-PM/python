# which function will you use to fetch all keys, all values, and key value pairs from a given dictionary?

dictionary={
    1:"A",
    2:"B",
    3:"C",
    4:"D"
}
keys=dictionary.keys()
values=dictionary.values()
keyValuePairs=dictionary.items()

print("KEYS = dictionary.keys() =",keys)
print("VALUES = dictionary.values() =",values)
print("KEY VALUE PAIRS = dictionary.items() =",keyValuePairs)